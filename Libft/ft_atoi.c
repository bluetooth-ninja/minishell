int	ft_atoi(char const *s)
{
	int	res;
	int	sign;

	res = 0;
	sign = 1;
	while (*s == '\t' || *s == '\n' || *s == ' ' || *s == '\v'
		|| *s == '\r' || *s == '\f')
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
	{
		res = res * 10 + *s - '0';
		s++;
	}
	return (res * sign);
}
